def convert_degrees_minutes_to_decimal(degree_str):
    """Convert degree string like '125Â° 38'' to decimal degrees"""
    try:
        if 'Â°' in degree_str and "'" in degree_str:
            parts = degree_str.replace("'", "").split('Â°')
            degrees = float(parts[0].strip())
            minutes = float(parts[1].strip())
            return degrees + (minutes / 60.0)
        else:
            return float(degree_str)
    except:
        return 0.0

